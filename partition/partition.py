def partition():
    with open('./All_labels.txt', 'r') as f:
        with open('./hair_train.txt', 'w') as train:
            with open('./hair_valid.txt', 'w') as valid:
                with open('./hair_test.txt', 'w') as test:
                    f.readline()
                    attr_name = f.readline().replace(',', ' ')
                    lines_train = [attr_name]
                    lines_valid = [attr_name]
                    lines_test = [attr_name]
                    for line in f.readlines():
                        line = line.replace(',', ' ')
                        rn = random.randint(0, 9)
                        if rn == 0:
                            lines_valid.append(line)
                        elif rn == 1:
                            lines_test.append(line)
                        else:
                            lines_train.append(line)

                    lines_train.insert(0, str(len(lines_train) - 1) + '\n')
                    lines_valid.insert(0, str(len(lines_valid) - 1) + '\n')
                    lines_test.insert(0, str(len(lines_test) - 1) + '\n')
                    train.writelines(lines_train)
                    valid.writelines(lines_valid)
                    test.writelines(lines_test)


